package game.Capability;
/**
 *  An enum representing the status if an EnemyCharacter has weapon in the game.
 *  @author MA_Lab06_Group2
 *  @version 1.0.0
 */
public enum HasWeapon {
    /**
     * Status is true if Enemy has Weapon
     */
    TRUE,
    /**
     * Status is false if Enemy has Weapon
     */
    FALSE
}
