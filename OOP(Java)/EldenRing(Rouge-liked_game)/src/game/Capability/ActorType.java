package game.Capability;
/**
 * ActorType class is a class that contains two different types of Actor
 * @author MA_Lab06_Group2
 * @version 1.0.0
 */
public enum ActorType {
    /**
     * Actor type is player
     */
    PLAYER,
    /**
     * Actor type is Enemy
     */
    ENEMY,
    /**
     * Revive if EnemyCharacter can be revived
     */
    REVIVE,
    /**
     * Actor is an ally
     */
    ALLY,

}
