package game.Capability;


/**
 * GroundType class is a class that contains two different types of ground.
 * @author MA_Lab06_Group2
 * @version 1.0.0
 */
public enum GroundType {
    /**
     * Status if stepping on the ground will kill the actor
     */
    FATAL,
    /**
     * Status if stepping on the ground does not kill the actor
     */
    NON_FATAL,
}
